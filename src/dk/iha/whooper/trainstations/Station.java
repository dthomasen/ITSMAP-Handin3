package dk.iha.whooper.trainstations;

public class Station {
	private String name;
	private int wid;
	private float x;
	private float y;

	public String getName() {
		return name;
	}
	
	public int getWid(){
		return wid;
	}
	
	public float getX(){
		return x;
	}
	
	public float getY(){
		return y;
	}
}
